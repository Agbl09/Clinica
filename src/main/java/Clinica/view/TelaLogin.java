    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String login_u = txtLogin.getText().toString();
        
        String senha_u = txtSenha.getText().toString();
        
        Usuário u = new Usuário();
        
        u.setlogin_u(login_u);
        u.setSenha_u(senha_u);
        
        u = UsuarioJPA.validarU(u);
        
        if(u != null)
        {
            JOptionPane.showMessageDialog(null, "Usuário " +u.getLogin_u()+ " autenticado!");
            JOptionPane.showMessageDialog(null, "Seu tipo é " +u.getTipo_u());
            
             new TelaMenu().setVisible(true);
            
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Usuário não foi encontrato. Tente Novamente");
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

